"""Визуальная «дорога на Остров»: прогресс-нить через шаги заявки.

Опрос — это не форма, а путь: от городской суеты и новостей к берегу Волги
и музыке в сосновом лесу. Каждый шаг подсвечивает прогресс-баром 🏙→🏝,
насколько гость уже близко к Острову, и ведёт его одной короткой подписью
всё дальше от города.
"""
from __future__ import annotations

# Этапы пути = экраны диалога заявки. Индекс показывает, насколько далеко
# гость ушёл от города в сторону Острова (0 — ещё в городе, последний — на месте).
STAGE_PACK = 0    # имя — глушим новости, собираем рюкзак
STAGE_ROAD = 1    # способ связи — выехали, суета позади
STAGE_VOLGA = 2   # билет — впереди блеснула Волга
STAGE_FOREST = 3  # подтверждение — заходим в сосновый лес, слышен бит
STAGE_ISLAND = 4  # готово — мы на Острове

# Подпись к каждому этапу: короткая, атмосферная, тянет всё дальше от города.
_CAPTIONS = (
    "🎒 Глушим ленту новостей и собираем рюкзак.",
    "🛣 Выехали — городская суета остаётся позади.",
    "🌊 Впереди блеснула Волга. Дышится уже легче.",
    "🌲 Заходим в сосновый лес, а оттуда — тёплый бит. 🎶",
    "🏝 Ты на Острове. Добро пожаловать домой!",
)

_TOTAL = len(_CAPTIONS)


def header(stage: int) -> str:
    """Двухстрочная «нить пути»: прогресс-бар 🏙→🏝 и подпись текущего этапа.

    Пройденные этапы — ▰, текущий — 📍, впереди — ▱. Возвращает HTML-строку
    для подстановки в начало сообщения шага.
    """
    stage = max(0, min(stage, _TOTAL - 1))
    cells = (
        "▰" if i < stage else "📍" if i == stage else "▱"
        for i in range(_TOTAL)
    )
    return f"🏙 {''.join(cells)} 🏝\n<i>{_CAPTIONS[stage]}</i>"


def with_step(stage: int, text: str) -> str:
    """Готовый текст шага: «нить пути» сверху, затем содержимое сообщения."""
    return f"{header(stage)}\n\n{text}"
