"""Подсказки «что на Острове прямо сейчас» по времени суток.

Делают атмосферу живой: ответ про музыку и приветствие подстраиваются под
текущий час, как будто Остров идёт прямо сейчас. Музыка и сцена делят одни
и те же «корзины» времени суток, чтобы не разъезжаться.
"""
from __future__ import annotations

from datetime import datetime


def _part(now: datetime | None = None) -> str:
    """Время суток: morning / day / evening / night."""
    hour = (now or datetime.now()).hour
    if 5 <= hour < 11:
        return "morning"
    if 11 <= hour < 17:
        return "day"
    if 17 <= hour < 22:
        return "evening"
    return "night"


# Образ звука — для ответа FAQ «Какая музыка?».
_MUSIC = {
    "morning": "🌅 Сейчас было бы раннее утро на берегу — тихие мелодичные сеты под плеск Волги.",
    "day": "☀️ Сейчас на Острове — лёгкий дневной сет: кто-то на сапе, кто-то на песке.",
    "evening": "🌇 Сейчас закат — темп растёт, сосновый лес начинает гудеть басом.",
    "night": "🌲 Сейчас была бы глубокая ночь — техно среди сосен до самого рассвета.",
}

# Образ всей сцены — для приветствия в /start.
_SCENE = {
    "morning": "🌅 На Острове сейчас раннее утро: туман над Волгой и кофе у тёплого костра.",
    "day": "☀️ На Острове сейчас день: сапы на воде, гамаки между сосен, лёгкий бит вдалеке.",
    "evening": "🌇 На Острове сейчас закат: небо над Волгой розовеет, лес оживает басом.",
    "night": "🌲 На Острове сейчас ночь: костры, техно среди сосен и звёзды над водой.",
}


def now_music_line(now: datetime | None = None) -> str:
    """Образная строка про звук Острова в текущее время суток."""
    return _MUSIC[_part(now)]


def now_scene_line(now: datetime | None = None) -> str:
    """Образная строка про всю сцену Острова в текущее время суток."""
    return _SCENE[_part(now)]
